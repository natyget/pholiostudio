/**
 * Analyzes an uploaded photo and returns predictions and market fit rankings.
 * 
 * Used by:
 * - /onboarding/upload (Phase A)
 * 
 * @param {string|Object} imageInput - Storage key (e.g. "uploads/filename.webp") OR absolute path, OR {storageKey: string}
 * @returns {Promise<Object>} Analysis results with embedding, predictions, markets, confidence
 */
async function analyzePhoto(imageInput) {
  let absolutePath;
  let storageKey;
  
  // Handle different input formats
  if (typeof imageInput === 'object' && imageInput.storageKey) {
    storageKey = imageInput.storageKey;
    const filename = path.basename(storageKey);
    absolutePath = path.join(config.uploadsDir, filename);
  } else if (typeof imageInput === 'string') {
    if (imageInput.startsWith('uploads/') && !path.isAbsolute(imageInput)) {
      storageKey = imageInput;
      const filename = path.basename(storageKey);
      absolutePath = path.join(config.uploadsDir, filename);
    } else {
      absolutePath = imageInput;
      storageKey = `uploads/${path.basename(imageInput)}`;
    }
  } else {
    throw new Error('Invalid imageInput: expected string (storageKey or absolute path) or object with storageKey property');
  }
  
  // Validate file exists
  if (!fs.existsSync(absolutePath)) {
    throw new Error(`Image file not found: ${absolutePath} (storageKey: ${storageKey})`);
  }

  // --- Real AI Analysis with Scout (Groq) ---
  try {
    const imageBuffer = fs.readFileSync(absolutePath);
    const base64Image = imageBuffer.toString('base64');
    // Simple mime type detection (fallback to jpeg)
    const ext = path.extname(absolutePath).toLowerCase();
    const mimeType = ext === '.png' ? 'image/png' : (ext === '.webp' ? 'image/webp' : 'image/jpeg');

    const scoutPrompt = `You are Scout, an elite physical analysis AI for Pholio. Analyze this image for physical measurements only.
Do not evaluate skin tone, hair color, or eye color.

Respond in exactly this JSON format. Return null for any field not confidently visible in the image.
{
  "height_estimate_cm": number or null,
  "bust_cm": number or null,
  "waist_cm": number or null,
  "hips_cm": number or null,
  "weight_kg": number or null,
  "build_type": "Petite | Slim | Athletic | Average | Curvy | Plus",
  "confidence": "Low | Medium | High",
  "visible_features": ["what was visible to make these estimates"]
}`;

    const completion = await groq.chat.completions.create({
      messages: [
        {
          role: 'user',
          content: [
            { type: 'text', text: scoutPrompt },
            { type: 'image_url', image_url: { url: `data:${mimeType};base64,${base64Image}` } }
          ]
        }
      ],
      model: 'meta-llama/llama-4-scout-17b-16e-instruct', // New explicit physical measurements model
      temperature: 0.1, // Lower temperature for more consistent/factual estimates
      max_completion_tokens: 1024,
      top_p: 1,
      response_format: { type: 'json_object' }
    });

    let content = completion.choices[0]?.message?.content || '{}';
    
    // Cleanup: Remove markdown code fences if present
    content = content.replace(/```json\n?|\n?```/g, '').trim();

    const aiResponse = JSON.parse(content);
    
    const predictions = {
      height_estimate_cm: aiResponse.height_estimate_cm || null,
      bust_cm: aiResponse.bust_cm || null,
      waist_cm: aiResponse.waist_cm || null,
      hips_cm: aiResponse.hips_cm || null,
      weight_kg: aiResponse.weight_kg || null,
      build_type: aiResponse.build_type || null,
      confidence: aiResponse.confidence || 'Low',
      visible_features: aiResponse.visible_features || []
    };

    if (predictions.confidence === 'Low') {
      console.warn('[AI Analysis] Scout returned Low confidence. Will not prefill frontend.');
    }

    return {
      embedding: generateMockEmbedding(), // Keep mock embedding for now
      predictions
    };

  } catch (error) {
    console.error('[AI Analysis] Error calling Scout/Groq:', error);
    
    // --- Fallback to Mock Data (so flow doesn't break) ---
    console.log('[AI Analysis] Falling back to mock data...');
    const embedding = generateMockEmbedding();
    const predictions = {
      height_estimate_cm: Math.floor(Math.random() * 30 + 160),
      weight_kg: Math.floor(Math.random() * 20 + 50),
      bust_cm: parseFloat((Math.random() * 20 + 76).toFixed(1)),
      waist_cm: parseFloat((Math.random() * 15 + 56).toFixed(1)),
      hips_cm: parseFloat((Math.random() * 20 + 81).toFixed(1)),
      build_type: 'Average',
      confidence: 'Medium',
      visible_features: []
    };
    
    return {
      embedding,
      predictions
    };
  }
}

